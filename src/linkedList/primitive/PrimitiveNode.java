package linkedList.primitive;

public class PrimitiveNode {
    private int i;
    private PrimitiveNode next;

    public PrimitiveNode(int i) {
        this.i = i;
    }

    public int getI() {
        return i;
    }

    public void setI(int i) {
        this.i = i;
    }

    public PrimitiveNode getNext() {
        return next;
    }

    public void setNext(PrimitiveNode next) {
        this.next = next;
    }

}
